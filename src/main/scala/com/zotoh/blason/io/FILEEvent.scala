package com.zotoh.blason
package io

import java.io.File

import com.zotoh.frwk.util.StrUtils.nsb
 import FILEAction._
  
/**
 * An Event generated by the FilePicker device.  This event carries the
 * reference to the file which has been picked up by the device.
 *
 * @author kenl
 */
@SerialVersionUID(4177245480806752339L)
class FILEEvent(src:EventEmitter, private var _file:File) extends AbstractEvent(src) {

  private var _action:FILEAction=null
  private var _origPath=""
  
  /**
   * @param dev
   * @param originalFile
   * @param f
   * @param e
   */
  def this(src:EventEmitter, originalFile:String, f:File, act:FILEAction) {
    this(src,f)
    _origPath=nsb(originalFile)
    _action=act
  }

  def action() = _action
  
  /**
   * @return
   */
  def origFilePath() = _origPath

  /**
   * @return
   */
  def file() = _file

}

